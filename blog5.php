                                            <p>Pengalaman membeli rumah pertama kali merupakan sebuah hal yang menyenangkan. Terlebih jika itu adalah rumah yang sudah lama kita idam-idamkan. Namun terkadang, karena ini adalah pengalaman pertama, ada beberapa hal yang belum diketahui. Hal ini tentu membuat kita banyak bertanya kepada penjual.</p>
											<br><br>Nah, daripada kurang update atau “kudet” dan kebanyakan tanya, lebih baik cari tahu dulu tentang beberapa hal yang biasanya ditanyakan oleh pembeli rumah pemula berikut ini.
											<br><br>1. Berapa jumlah uang yang harus saya siapkan saat pertama kali membeli rumah?
											<br><br><img src="image/bg5.jpg" alt="">
											<br><br>Saat membeli rumah, hal pertama yang harus kita siapkan adalah uang. Namun terkadang, kita sering bingung dengan jumlah uangnya. Jika membeli dengan cara cash atau tunai, tentu akan lebih mudah menghitungnya. Tapi, kalau membelinya dengan cara KPR, tentu kita harus memperhitungkan dahulu uang yang harus disiapkan.
											<br><br>Agar mudah menghitungnya, pahami dulu rumusan sederhana ini.
											<br><br>Harga rumah : 100 = cicilan per bulan yang harus dibayarkan.
											<br><br>Jadi jika kamu membeli rumah seharga 400 juta makan cicilan perbulan kurang lebih adalah 4 jutaan. Dan begitu seterusnya untuk harga rumah yang 500 juta ke atas.
											<br><br>Misal kita akan membeli rumah di Perumahan Permata Bogor Residence seharga 400 jutaan, maka cicilan perbulannya sekitar 4 juta. Sehingga minimal gaji yang kita miliki adalah 7-8 juta. Hal ini berdasarkan perhitungan bahwa pihak bank akan memperhitungkan kemampuan membayar seseorang sebesar 55% dari take home pay. Tapi tenang saja, gaji 8 juta adalah gabungan penghasilan dari suami dan istri. Jadi jika kamu dan pasangan gajinya masing-masing Rp 4 juta, sudah bisa mengajukan KPR di Perumahan Permata Bogor Residence.
											<br><br>2. Dimana lokasi rumah yang tepat untuk saya?
											<br><br><img src="image/bg55.jpg" alt="">
											<br><br>Terkadang pertanyaan ini juga membuat kita bingung. Terlebih rumah adalah sesuatu yang tidak bisa kita pindah seenaknya. Maka dari itu, penentuan lokasi rumah sangatlah penting, termasuk faktor kenyamanan dan jarak dengan tempat kerja.<br><br>Kalau kamu mau membeli rumah di bawah ukuran 70 m2, umumnya kamu harus membayar DP 10% dari harga rumah. Jangan lupa juga sisihkan uang untuk pelunasan “sisa DP” supaya palfond kredit yang kamu ajukan nggak turun. Jika sudah terkumpul cukup uang untuk membayar DP rumah, kita dapat langsung melanjutkan ke proses booking rumah impian dan akad kredit hanya dalam waktu 2 sampai 3 bulan, lho. Cepat kan? Makanya yuk mulai nabung dulu agar lekas mendapatkan rumah impian!
											<br><br>Maka dari itu, carilah lokasi yang strategis dan dekat dengan fasilitas umum. Misalnya 5 menit dari stasiun kereta, tidak jauh dari kawasan kota, serta dekat dengan berbagai sarana umum seperti rumah sakit, sekolah dan pasar swalayan.
											<br><br>3. Lebih baik membeli rumah sekarang atau menunggu tabungan cukup?<br><br><img src="image/bg11.jpg" alt="">										
											<br><br><img src="image/bg555.jpg" alt="">
											<br><br>Ini adalah salah satu pertanyaan yang sering ditanyakan oleh pembeli pemula. Sebenarnya, lebih baik kamu membeli rumah saat ini juga. Karena nilai suku bunga biasanya berubah-ubah setiap tahunnya. Selain itu harga rumah juga cenderung meningkat di tahun berikutnya.
											<br><br>Nah, daripada nunggu beberapa tahun lagi, lebih baik beli rumah dari sekarang saja. Meski tabunganmu belum cukup, kamu bisa memilih cara KPR untuk membeli rumah. Dengan ini keinginan untuk membeli rumah bisa kamu laksanakan segera.
											<br><br>4. Mending beli rumah dengan cara KPR atau cash keras ya?
											<br><br><img src="image/bg5555.jpg" alt="">
											<br><br>Membeli rumah dengan cara KPR atau cash juga menjadi salah satu hal yang membingungkan. Tapi, sebenarnya keduanya memiliki kelebihan dan kekurangannya masing-masing.
											<br><br>Jika kamu memilih untuk membeli rumah dengan cara KPR maka kelebihannya adalah tidak perlu menyiapkan dana besar, cicilan ringan, dan bisa ditinggali meski belum lunas. Sedangkan kekurangannya adalah prosesnya yang cukup rumit, total uang yang dibayarkan lebih banyak daripada tunai, dan suku bunga fluktuatif.
											<br><br>Untuk kamu yang memilih membeli rumah dengan cara tunai, kelebihannya adalah harga lebih murah, rumah lebih cepat ditempati, lebih mudah, dan terkadang ada juga developer yang memberi diskon lebih. Kalau kekurangan membeli rumah dengan cara tunai adalah dana yang dikeluarkan tidak sedikit dan ada resiko jika membeli rumah yang bermasalah.
											<br><br>5. Sebaiknya beli rumah atau apartemen ya?
											<br><br><img src="image/bg55555.jpg" alt="">
											<br><br>Biasanya yang bingung dengan pilihan rumah atau apartemen adalah generasi milenial yang bekerja di kota besar. Banyak orang tergiur dengan apartemen yang lokasinya berada ditengah kota. Namun, sebenarnya rumah yang berada di atas tanah langsung nilainya lebih tinggi daripada apartemen, karena saat ini keberadaan tanah lebih langka. Apalagi jika nanti rumah semakin padat, pastinya harganya semakin mahal. Selain itu tinggal dirumah yang berada diatas tanah langsung juga rasanya juga akan lebih nyaman. Terlebih kita juga jadi punya halaman sendiri.
											<br><br>Nah, buat kamu yang masih bingung, coba dipikir lagi apa yang jadi kebutuhanmu. Pertimbangkan pula nilai investasi dan kenyamanannya. Jangan terburu-buru dan terlalu tergiur dengan sales yang menawarkan properti.
											<br><br>6. Apakah saya harus melakukan pengecekan bangunan?
											<br><br><img src="image/bg555555.jpg" alt="">
											<br><br>Tentu saja hal ini wajib dilakukan saat membeli rumah. Jangan sampai kamu menyesal karena tidak melihat kualitas bangunan rumah yang akan kamu beli. Saat datang ke lokasi, cek juga aliran air, pemasangan pipa, kabel listrik, dan kualitas bangunannya. Biasanya setelah serah terima rumah, konsumen diberi waktu 3 bulan untuk komplain ke developer jika ada yang salah dan tidak pas. Maka dari itu, hal ini harus kamu manfaatkan sebaik-baiknya. Dan jangan undur waktu serah terima dari developer ke konsumen karena kamu yang akan rugi sendiri.
											<br><br>7. Apakah setelah transaksi rumah bisa langsung ditempati?
											<br><br><img src="image/bg5555555.jpg" alt="">
											<br><bR>Pertanyaan ini adalah pertanyaan umum yang sering ditanyakan. Karena memang saat membeli rumah, kita akan tidak sabar untuk menempatinya. Nah, perlu kamu tahu bahwa ada rumah yang bisa langsung ditempati, namun ada juga rumah yang tipenya baru dibangun setelah dibeli. Agar lebih pasti, lebih baik kamu tanyakan kepada penjual atau developer saat melakukan transaksi. Apakah rumah tersebut sudah tersedia dan siap dihuni atau masih dalam proses pembangunan.
											<br><br>Beberapa pertanyaan tersebut memang sering ditanyakan oleh pembeli. Terutama jumlah uang yang disiapkan dan lokasi rumah. Agar tidak salah pilih, memang lebih baik hal ini kamu tanyakan secara detail kepada penjual saat bertransaksi. Dan pastikan bahwa rumah yang akan kamu beli memiliki banyak kelebihan dan menguntungkan seperti di Permata Bogor Residence. 
